Color = namedtuple('RGB', 'red, green, blue')
named = {}  # dict of colors


class RGB(Color):
    def hex_format(self):
        """Returns color in hex format"""
        return '#{:02X}{:02X}{:02X}'.format(self.red, self.green, self.blue)


